def _entry(word: str, rank: int) -> VocabularyEntry:
    return VocabularyEntry(
        word=word,
        pos="phrase" if len(word) > 4 else "noun",
        definition=f"「{word}」は日本語学習で使う重要語です。",
        example=f"{word}について日本語で説明します。",
        frequency_rank=rank,
    )


