def _question(
    question_id: str,
    skill: Skill,
    difficulty: CEFRLevel,
    question: str,
    correct: str,
    distractors: list[str],
    grammar_slug: str | None = None,
) -> AssessmentQuestion:
    return AssessmentQuestion(
        id=question_id,
        skill=skill,
        difficulty=difficulty,
        question=question,
        options=[correct, *distractors],
        correct=correct,
        grammar_slug=grammar_slug,
    )


def _build_bank() -> list[AssessmentQuestion]:
    bank: list[AssessmentQuestion] = []
    for level in ("A1", "A2", "B1", "B2", "C1", "C2"):
        for index, (slug, question, correct, distractors) in enumerate(_GRAMMAR[level], start=1):
            bank.append(
                _question(
                    f"ja-g-{level.lower()}-{index:03}",
                    "grammar",
                    level,
                    question,
                    correct,
                    distractors,
                    slug,
                )
            )
        for index, (question, correct, distractors) in enumerate(_VOCABULARY[level], start=1):
            bank.append(
                _question(
                    f"ja-v-{level.lower()}-{index:03}",
                    "vocabulary",
                    level,
                    question,
                    correct,
                    distractors,
                )
            )
        for index, (question, correct, distractors) in enumerate(_READING[level], start=1):
            bank.append(
                _question(
                    f"ja-r-{level.lower()}-{index:03}",
                    "reading",
                    level,
                    question,
                    correct,
                    distractors,
                )
            )
    return bank


ASSESSMENT_BANK: list[AssessmentQuestion] = _build_bank()
