def build_set(level: CEFRLevel, unit_ref: str, spec: SetSpec) -> VocabularySet:
    """Build a single-word vocabulary set from a spec tuple.

    Produces one clean entry per spec — no auto-generated compound words.
    """
    set_id, topic, headword = spec
    definition, example = _ENTRY_CONTENT.get(
        headword,
        (f"{headword}——{topic}领域的核心词汇。", f"请用「{headword}」造一个与{topic}相关的句子。"),
    )
    return VocabularySet(
        id=set_id,
        level=level,
        topic=topic,
        unit_ref=unit_ref,
        words=[
            VocabularyEntry(
                word=headword,
                pos=_pos_for(headword),
                definition=definition,
                example=example,
                ipa=_PINYIN.get(headword),
                frequency_rank=None,
            ),
        ],
    )
