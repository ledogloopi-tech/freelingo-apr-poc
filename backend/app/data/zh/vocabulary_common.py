def _pos_for(word: str) -> PartOfSpeech:
    """Return a reasonable part-of-speech for the given headword."""
    _POS_MAP: dict[str, PartOfSpeech] = {
        "因为": "conjunction",
        "如果": "conjunction",
        "下雨": "verb",
        "运动": "verb",
        "打扫": "verb",
        "维修": "verb",
        "换乘": "verb",
        "租房": "verb",
        "汇报": "verb",
        "办理": "verb",
        "选择": "verb",
        "请": "verb",
        "修改": "verb",
        "解释": "verb",
        "好": "adjective",
        "高兴": "adjective",
        "更好": "adjective",
    }
    return _POS_MAP.get(word, "noun")


def build_set(level: CEFRLevel, unit_ref: str, spec: SetSpec) -> VocabularySet:
    """Build a single-word vocabulary set from a spec tuple.

    Produces one clean entry per spec — no auto-generated compound words.
    """
    set_id, topic, headword = spec
    return VocabularySet(
        id=set_id,
        level=level,
        topic=topic,
        unit_ref=unit_ref,
        words=[
            VocabularyEntry(
                word=headword,
                pos=_pos_for(headword),
                definition=f"{headword}——{topic}领域的核心词汇。",
                example=f"请用「{headword}」造一个与{topic}相关的句子。",
                ipa=_PINYIN.get(headword),
                frequency_rank=None,
            ),
        ],
    )
