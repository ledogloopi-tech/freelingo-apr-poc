def build_topic(level: CEFRLevel, spec: TopicSpec) -> GrammarTopic:
    slug, title, category, structure, example = spec[:5]
    info = _TOPIC_CONTENT.get(slug, {})
    common_mistakes = (
        spec[5]
        if len(spec) > 5
        else [
            GrammarMistake(
                wrong="只记形式，不注意语序、搭配或语气。",
                correct="把语法点放进完整句子里，检查语序、搭配和上下文。",
                note="现代汉语的语法常依靠词序、虚词和语境共同表达意思。",
            )
        ]
    )
    return GrammarTopic(
        slug=slug,
        title=title,
        level=level,
        category=category,
        summary=info.get("summary", _DEFAULT_SUMMARY),
        explanation=info.get("explanation", _DEFAULT_EXPLANATION),
        structure=structure,
        rules=[
            "注意中文基本语序和前后成分的搭配。",
            "根据语境判断语气、时间和信息重点。",
            "先用短句练习，再扩展到对话和段落。",
        ],
        examples=[
            GrammarExample(
                text=example,
                note=info.get("example_note", "自然用法示例"),
            )
        ],
        common_mistakes=common_mistakes,
        related=[],
    )
