async function renderRecording(strict = false) {
  mockApiFetch.mockResolvedValueOnce(jsonResponse(manifest))
  if (strict) mockApiFetch.mockResolvedValueOnce(jsonResponse(manifest))
  const ui = <AprLessonPlayer endpoint={endpoint} />
  render(strict ? <StrictMode>{ui}</StrictMode> : ui)
  await screen.findByText('Orientation')
  fireEvent.click(screen.getByRole('button', { name: 'Continue' }))
  fireEvent.click(screen.getByRole('button', { name: 'Continue' }))
  fireEvent.click(screen.getByLabelText('OK'))
  fireEvent.click(screen.getByRole('button', { name: 'Continue' }))
}

async function confirmOriginal(text = 'olá confirmado') {
  fireEvent.click(screen.getByRole('button', { name: 'Start recording' }))
  mockApiFetch.mockResolvedValueOnce(jsonResponse({ draft_text: text }))
  fireEvent.click(
    screen.getByRole('button', { name: 'Generate transcript draft' })
  )
  await screen.findByDisplayValue(text)
  fireEvent.click(
    screen.getByRole('button', { name: 'Confirm reviewed transcript' })
  )
}

function feedbackCalls() {
  return mockApiFetch.mock.calls.filter(([url]) => url === feedbackEndpoint)
}

describe('APR feedback retry', () => {
  beforeEach(() => {
    mockApiFetch.mockReset()
    vi.stubGlobal('URL', {
      createObjectURL: vi.fn(() => `blob:${Math.random()}`),
      revokeObjectURL: vi.fn(),
    })
    vi.spyOn(window, 'confirm').mockReturnValue(true)
  })

  it('is ineligible before confirmation and never requests automatically through render, capture, STT, draft, edit, or confirmation', async () => {
    await renderRecording()
    expect(feedbackCalls()).toHaveLength(0)
    fireEvent.click(screen.getByRole('button', { name: 'Start recording' }))
    expect(
      screen.getByText(/Confirm the Original transcript/)
    ).toBeInTheDocument()
    expect(feedbackCalls()).toHaveLength(0)
    mockApiFetch.mockResolvedValueOnce(jsonResponse({ draft_text: 'máquina' }))
    fireEvent.click(
      screen.getByRole('button', { name: 'Generate transcript draft' })
    )
    await screen.findByDisplayValue('máquina')
    fireEvent.change(screen.getByLabelText('Reviewed transcript correction'), {
      target: { value: 'revisto' },
    })
    fireEvent.click(
      screen.getByRole('button', { name: 'Confirm reviewed transcript' })
    )
    expect(
      screen.getByRole('button', { name: 'Generate technical feedback' })
    ).toBeInTheDocument()
    expect(feedbackCalls()).toHaveLength(0)
  })

  it('sends exactly the controlled request body after explicit activation and displays the controlled response', async () => {
    await renderRecording()
    await confirmOriginal()
    mockApiFetch.mockResolvedValueOnce(jsonResponse(feedback))
    fireEvent.click(
      screen.getByRole('button', { name: 'Generate technical feedback' })
    )
    expect(
      screen.getByText('Generating technical feedback…')
    ).toBeInTheDocument()
    await screen.findByText('Controlled technical feedback')
    const [, init] = feedbackCalls()[0]
    expect(init?.headers).toEqual({ 'Content-Type': 'application/json' })
    expect(JSON.parse(String(init?.body))).toEqual({
      feedback_id: feedbackId,
      attempt_role: 'original',
      transcript_confirmation_revision: 1,
    })
    expect(String(init?.body)).not.toContain('olá confirmado')
    expect(String(init?.body)).not.toContain('audio')
    expect(screen.getByText(feedback.acknowledgement)).toBeInTheDocument()
    expect(screen.getByText(feedback.primary_priority)).toBeInTheDocument()
    expect(screen.getByText(feedback.cue)).toBeInTheDocument()
    expect(screen.getByText(feedback.retry_instruction)).toBeInTheDocument()
    expect(screen.getByText(feedback.uncertainty)).toBeInTheDocument()
    expect(
      screen.getByText('Session-only technical placeholder')
    ).toBeInTheDocument()
    expect(
      screen.getByText('This feedback does not evaluate your Portuguese.')
    ).toBeInTheDocument()
    expect(
      screen.queryByText(/CEFR|proficiency|incorrect|language assessment/i)
    ).not.toBeInTheDocument()
  })

  it('increments revisions and invalidates stale feedback after re-confirmation', async () => {
    await renderRecording()
    await confirmOriginal('primeiro')
    mockApiFetch.mockResolvedValueOnce(jsonResponse(feedback))
    fireEvent.click(
      screen.getByRole('button', { name: 'Generate technical feedback' })
    )
    await screen.findByText('Controlled technical feedback')
    fireEvent.change(screen.getByLabelText('Reviewed transcript correction'), {
      target: { value: 'segundo' },
    })
    fireEvent.click(
      screen.getByRole('button', { name: 'Confirm reviewed transcript' })
    )
    expect(
      screen.queryByText('Controlled technical feedback')
    ).not.toBeInTheDocument()
  })

  it('distinguishes pre-feedback and post-feedback retries while preserving navigation and restart behavior', async () => {
    await renderRecording()
    await confirmOriginal()
    fireEvent.click(
      screen.getByRole('button', { name: 'Record another attempt' })
    )
    mockApiFetch.mockResolvedValueOnce(jsonResponse(feedback))
    fireEvent.click(
      screen.getByRole('button', { name: 'Generate technical feedback' })
    )
    await screen.findByText('Waiting for an optional post-feedback retry.')
    fireEvent.click(
      screen.getByRole('button', { name: 'Record another attempt' })
    )
    expect(
      screen.getByText('Post-feedback retry captured.')
    ).toBeInTheDocument()
    expect(
      screen.getByText(/does not indicate improvement or a language result/)
    ).toBeInTheDocument()
    fireEvent.click(screen.getByRole('button', { name: 'Continue' }))
    fireEvent.click(screen.getByRole('button', { name: 'Back' }))
    expect(
      screen.getByText('Controlled technical feedback')
    ).toBeInTheDocument()
    vi.mocked(window.confirm).mockReturnValueOnce(false)
    fireEvent.click(screen.getByRole('button', { name: 'Restart' }))
    expect(
      screen.getByText('Post-feedback retry captured.')
    ).toBeInTheDocument()
    vi.mocked(window.confirm).mockReturnValueOnce(true)
    fireEvent.click(screen.getByRole('button', { name: 'Restart' }))
    expect(
      screen.queryByText('Controlled technical feedback')
    ).not.toBeInTheDocument()
  })

  it('handles technical failure non-judgmentally without blocking Continue and supports Strict Mode', async () => {
    await renderRecording(true)
    await confirmOriginal()
    mockApiFetch.mockResolvedValueOnce(jsonResponse({ detail: 'down' }, 503))
    fireEvent.click(
      screen.getByRole('button', { name: 'Generate technical feedback' })
    )
    await screen.findByText(
      'APR could not load technical feedback. This is a feedback-service issue, not a language result.'
    )
    expect(
      screen.getByRole('button', { name: 'Try technical feedback again' })
    ).toBeInTheDocument()
    fireEvent.click(screen.getByRole('button', { name: 'Continue' }))
    expect(screen.getByText('Reflection')).toBeInTheDocument()
  })

  it('aborts active feedback on confirmed restart and unmount', async () => {
    const pending = deferredResponse()
    await renderRecording()
    await confirmOriginal()
    mockApiFetch.mockReturnValueOnce(pending.promise)
    fireEvent.click(
      screen.getByRole('button', { name: 'Generate technical feedback' })
    )
    const signal = feedbackCalls()[0][1]?.signal as AbortSignal
    fireEvent.click(screen.getByRole('button', { name: 'Restart' }))
    expect(signal.aborted).toBe(true)
    pending.resolve(jsonResponse(feedback))
    await waitFor(() =>
      expect(
        screen.queryByText('Controlled technical feedback')
      ).not.toBeInTheDocument()
    )
  })
})
